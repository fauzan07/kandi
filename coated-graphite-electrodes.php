                        <p>Coating consists of 3 layers serving separate function. The first layer produces a high temperature, oxidation resistant coating, which forms the major protection of the electrode. Above this layer, a sprayed coat helps to nullify any porosity of the previous coating layer. The final layer is mainly responsible for the increase in conductivity. The coating withstands temperatures over 1850 deg C and chemically destroyed only after many hours in the furnace. It is relatively impermeable to gas, and being semi-fluid high aluminium layer, resistant to thermal shock.</p>
                        <p>The degree of oxidation is determined by measuring the carbon monoxide and carbon dioxide present in waste gases extracted during the heating of samples in an oxidising gas stream. At temperatures between 1000 ~ 1850 deg. C found in arc furnace, the uncoated graphite samples yield large amounts of CO &amp; CO2. By contrast, at 1000˚ C, the coated samples produce no CO or CO2, and even after some time at 1850 deg. C, no CO and only 2~5% of CO2 is found. The electrical resistance is reduced ( and hence conductivity increased ) by the application of coating. Laboratory tests have shown the coating itself to have an electrical resistance of 14.50-22.00µ ohm cm against that of graphite of 600~1000 µ ohm cm.</p>
