                        <p>Graphite Electrodes</p>
                        <p>Graphite electrodes are used mainly in electric arc furnace steel production. Furthermore, coatings has been used on <strong>graphite electrodes</strong> since 1969 in these industries for steel melting, ranging from 5 tonnes to 185 tonnes capacity, using 200 ~ 600 mm diameter electrodes. Graphite electrodes are used for producing STEEL.</p>
                        <p>The coating has two main functions :</p>
                        <ul class="list">
                        <li>To protect the surface of the electrode while use in the furnace from oxidation. While the oxidation occurs, the diameter of the electrode tip remains larger than that on an uncoated electrode. The linear rate of wear of the electrode is also decreased so that the overall electrode consumption is reduced.</li>
                        <li>To form high conductivity, low resistivity skin which increase the current carrying capacity of the electrode. This allows more current to pass through the same sized electrode, avoiding severe mechanical damage.</li>
