                        <strong>Burning / Thermal Lance</strong> is the product developed for the purpose of increasing the heat quality generated from the lance, by improving the cutting efficiency with the cutting core wires of several pieces built in the combustion steel pipe. The difficult to &#8211; cut materials in gas cutting and mechanical cutting is not limited to iron and steel, stainless steel, alloys such as brass, nonferrous metal, ceramic etc. These can be simply and quickly cut off and board without noise and vibration at the high temperature approx 3600 <sup>o</sup>F. It is optimum for cutting and boring concrete, special steel, cast steel, pig iron, slag and refractory. <strong>Oxygen gas is the only heat source required for the equipment.</strong></p>
                        <h5 class="main_title my-3">WHAT IS THE DIFFERENCE BETWEEN THERMAL AND EXOTHERMIC CUTTING?</h5>
                        <p>The Thermal Cutting Torch uses a preheat flame to elevate the temperature of the material to be cut to 1500 to 1900 <sup>o</sup>F. When the steel become cherry red a high pressure jet of oxygen is directed the pre-heated metal. The result is the metal is rapidly oxidized or it burns. This process works on material that will oxidize. If it will not rust, it cannot be cut readily cut with a Thermal system.</p>
                        <p><strong>Cutting steel with an exothermic torch requires no preheat or cleaning.</strong></p>
                        <p>Exothermic process also utilizes oxidation. However, the exothermic process uses alloys in the material in the Burning / Thermal Lance as the fuel and produces temperatures of 7200 <sup>o</sup>F. Burning / Thermal Lance consists of an outer tube filled with special alloy wire. The exothermic reaction (Oxidation) occurs when the tip of the rod is heated to its kindling temperature (2800 <sup>o</sup>F) and pure oxygen is introduced through the lance. The chemical reaction will continue until the lance is consumed or the oxygen flow is turned off. <strong>The only gas required for this cutting operation is pure oxygen.</strong></p>
                        <h5 class="main_title my-3">WHAT IS THE MOST CRITICAL PART OF THE PROCESS?</h5>
                        <p><strong>The exothermic process relies on OXYGEN VOLUME and PRESSURE.</strong> Without proper volume of oxygen the cutting process will not work and creates hazards to the operator. Minimum and maximum volumes and pressures are listed with each size burning lance. You <strong>MUST</strong> supply the minimum volume and pressure for safe efficient operation. For all of the large burning lance you <strong>CAN NOT</strong> operate them with a single liquid oxygen tank.</p>
