                        <h5 class="main_title my-4">What is the purpose of Burning Lance</h5>
                        <p>Burning Lance is the product developed for the purpose of increasing the heat quality generated from the lance, by improving the cutting efficiency with the cutting core wires of several pieces built in the combustion steel pipe.</p>
                        <h5 class="main_title my-4">What is so special about Burning Lance as compared to conventional cutting system used</h5>
                        <p>The lance will cut most ferrous and non-ferrous metals as well as concrete and refractory. Benefits over conventional cutting methods include cutting speed and the ability to cut thick rusty metals.</p>
                        <h5 class="main_title my-4">What are the diameters and sizes of Burning Lance</h5>
                        <p>The standard diameters and lengths are 12.7 mm O.D., 13.5mm O.D., 17mm O.D., 21.7 mm and 27.2 mm O.D.<br />
                        Length &#8211; 2750mm, 3000mm or as per customer&#8217;s requirement.</p>
                        <h5 class="main_title my-4">If the customer wants any other length and sizes, can the lance be supplied?</h5>
                        <p>Yes, we can meet the customer&#8217;s requirement in length and diameters to meet their specifications, provided the minimum quantity of requirements is met.</p>
                        <h5 class="main_title my-4">What are the end finish of the Burning Lance for connecting to the holder?</h5>
                        <p>The end finish is the Plain End, Threaded and Coupled.</p>
                        <h5 class="main_title my-4">What is the advantage of using a Burning Lance for Cutting?</h5>
                        <p>The difficult to cut materials in gas cutting and mechanical cutting i.e. not only limited to iron and steel, the stainless steel, alloys such as brass, non-ferrous metals , ceramic, etc. can be simply and quickly cut off and bored without noise and vibration at the high temperature of 3600 <sup>o</sup>C. It is optimum for cutting and boring special steel, cast steel, pig iron, slag and concrete. Heat source is not required for the equipment.</p>
                        <h5 class="main_title my-4">What are the equipments required while using Burning Lance?</h5>
                        <p>Oxygen Cutting rods, Lance holder set, sufficient oxygen supply (35 cubic / min, each rod burns for four and half minutes &#8211; approx. 160 total cubic feet). Oxygen hose: 25’ – 50’ of 3/8” or ½” (smaller than 3/8” hose will reduce the flow of oxygen beyond effective operation). Oxygen high volume regulator (head pressure 200 PSI required). Ignition equipment require for Oxy-Acetylene Torch and Flame resistant protective clothing.</p>
                        <h5 class="main_title my-4">After connecting the holder of lance, what is the subsequent operation?</h5>
                        <p>To start the operation, the end of the lance is heated to ignition temperature by means of an Oxy / Acetylene torch. Oxygen is then fed through the lance promoting fusion at the lance point, and the reaction becomes self supporting.</p>
                        <h5 class="main_title my-4">Is boring operation in concrete possible, and what are the techniques?</h5>
                        <p>The boring operation is not difficult, the lance should be fed into the hole as boring progresses. Holes 3 meters in length can be bored horizontally, although it would be better to incline the object, if possible, to allow the slag to flow freely out.<br />
                        Boring vertically down can be carried out successfully, but after the first 12&#8243; the Oxygen pressure has to be increased to expel the molten slag. The slag will be expelled from the front until you have pierced through the complete thickness. Slag can therefore be thrown several feet towards the operator.<br />
                        When starting, if a free edge is not available to commence the boring then the technique is to pierce a hole through completely in the middle. When boring the lance is kept aimed slightly downward and the slag kept in motion by forwards and backwards motion. Sand is placed beneath the slag to protect the floor.</p>
                        <h5 class="main_title my-4">What are general benefits of Burning Lance?</h5>
                        <p>It is simple one man operation and extremely mobile. Under water cutting is possible. Equipment cost is low. The operation speed is high and makes little or no noise while cutting. The cost is reduced / low compared to Oxy-Acetylene cutting.</p>
                        <h5 class="main_title my-4">What should be the Oxygen pressure?</h5>
                        <p>Adjust the Oxygen between 9 to 12 bar</p>
                        <h5 class="main_title my-4">Does the hardness of concrete and steel diminish the cutting ability of the lance?</h5>
                        <p>No, the greater the hardness of concrete or steel, the better the cutting action of the Burning Lance.</p>
                        <h5 class="main_title my-4">Does cutting of concrete affect the quality of other concrete structure just adjacent?</h5>
                        <p>Generally speaking, leaving the linear area of about 4 inches of the cut, the concrete structure does not get affected. However, it is suggested that the water is sprayed on the concrete after cutting so that the adjacent areas of structures is safe and cooled after the operation.</p>
