                            mild steel lance pipe. By calorisation, a thickness of 100-150 microns is imparted on outer
                            surface, and further coating with refractory materials in 200-300 microns thickness on both
                            inner and outer surfaces, the diffusion is an inter metallic bond, which doesn&#8217;t get
                            damaged either by mechanical working like bending or straightening or by high temperatures.
                            In the case of metal diffused pipes, the metal existing at the surface of the diffused zone
                            is oxidized to the respective oxide, which prevents the further progress of oxidation and
                            also melting. For instance, by calorisation, aluminium is oxidized alumina, which has a
                            <strong>very high melting point of 2050 <sup>o</sup>C</strong> compared to the melting point
