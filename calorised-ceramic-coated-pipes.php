                        <h5 class="main_title my-3">Why Calorised Lance Pipes were developed for oxygen steel making?
                        </h5>
                        <p>In the process of steel manufacturing by open hearth or electric furnace, the consumption
                            rate of lance pipes for oxygen injection slows rather high ratio owing to high temperature
                            severe oxidation. In general, steel pipes are used as lance pipes for oxygen
                            injection.<strong> If MS pipes are treated by Calorisation process, consumption rate of
                                lance pipes will decrease 5-8 times.</strong> Research conducted over many years
                            revealed that the most effectual method is to diffuse aluminum into the pipe&#8217;s
                            surface, so as to promote where proof properties on steel pipe.<b> Calorising is carried
                                outside of the steel pipe to promote wear and fire proof properties of the pipe.</b></p>
                        <img src="images/CCCP-elect2.png" class="my-4 img-fluid" alt="Responsive image">
                        <p>Cross Section of Electric Arc Furnace showing Lance position</p>
                        <p>When Calorised pipe is exposed to high heat, the refractory material and the Calorised layer
                            form an Aluminium Oxide Film which increases the melting temperature to <strong>2050
                                <sup>o</sup>C (3750 <sup>o</sup>F). Bare lance pipe melts at temperatures of 1300 to
                                1500 <sup>o</sup>C (2400 to 2800 <sup>o</sup>F)</strong></p>
                        <h5 class="main_title my-3">Calorisation On Ceramic Coated Pipe</h5>
                        <p>MS pipes are <strong>calorised</strong> on outer surface, and are thereafter given
                            <strong>Ceramic Coating</strong> on both inner and outer surface after the calorizing
                            treatment. Ceramic coating gives an extra ordinary higher life and resistance to corrosion
                            at high temperature than ordinary MS pipes and expensive stainless and alloy steel pipes
                            normally used in oxygen lancing in electric arc furnaces and chemical factories and
                            refineries. <strong>The average life of calorised with ceramic coated pipes is 5-8 times
                                more than that of ordinary MS Pipes and Stainless Steel Pipes.</strong></p>
