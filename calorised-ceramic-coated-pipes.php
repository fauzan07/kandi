                        <p>To enumerate the advantages of these pipes in oxygen steel making process are:</p>
                        <ul class="list">
                            <li>The exothermic reaction and agitation promote decarbonisation and heat rise in the furnace, while foaming slag can be eliminated.</li>
                            <li>Fusion of sub material can be accelerated.</li>
                            <li>Quality of steel will be improved.</li>
                            <li>As the process raises the temperature.</li>
                            <li>Selection of raw materials to be charged becomes easy.</li>
                            <li>The process raises the production capacity of an electric furnace.</li>
                            <li>Hydrogen,Nitrogen and non-metallic inclusions can be eliminated through oxidation.</li>
                            <li>It makes it possible to recover chrome with the use of high chrome steel scrap.</li>
                        </ul>
                        <p><strong>To summarize</strong>, Calorised &amp; Ceramic Coated Pipe (CCCP) is carbon steel
                            pipe with calorisation on outer surface of pipe, and thereafter multiple, thin layers of
                            ceramic coating both exteriorly and interiorly. The outer surface of ceramic coating on pipe
                            is resistant to slag attack and extends the life of the pipe when it is immersed into molten
                            metal or in high temperature atmospheres. The CCCP applications include powder injections,
                            tap-hole cleaning and opening nozzles is ladles, tundish and launders.</p>
